class Calculator {
    constructor(products) {
        this.products = products;
    }

    findBestproduct (deposit, bankProducts) {
        let maxTotal = 0;

        for (let i = 0; i < bankProducts.length; i++) {

            let total = this.calculator(deposit.userSumm, bankProducts[i].incomeType, deposit.userTerm, deposit.userDeposit);

            if (total > maxTotal) {
                maxTotal = total

            }
        }


        bankProducts = bankProducts.filter( (element) => {
            let total = this.calculator(deposit.userSumm, element.incomeType, deposit.userTerm, deposit.userDeposit);
            if (total === maxTotal) {
                return true;
            } else {
                return false;
            }

        });
        return bankProducts;
    }

    filterBanks = (bankData, userCurrency, userDeposit, userSumm, userTerm) => {
        console.log(bankData, userCurrency, userDeposit, userSumm, userTerm);
        let result = bankData;
        result = result.filter(function (element) {
            if (element.currency === userCurrency) {
                return true
            }
        });

        result = result.filter(function (element) {
            if (userDeposit > 0) {
                if (element.canDeposit === true) {
                    return true;
                } else {
                    return false;
                }
            } else {
                return true;
            }
        });


        result = result.filter(function (element) {
            console.log(element.termMax, element.termMin, userTerm)
            if (userTerm >= element.termMin && (userTerm <= element.termMax || element.termMax === null)) {
                return true;
            } else {
                return false;
            }


        });


        result = result.filter(function (element) {
            if ((userSumm >= element.sumMin) && (userSumm <= element.sumMax || element.sumMax === null)) {
                return true;
            } else {
                return false;
            }

        });


        return result;
    }
    calculator = (summ, interest, term, deposit) => {
        for (let i = 0; i < term; i++) {
            summ *= (1 + interest / 100 / 12);
            summ += deposit;
        }
        summ -= deposit;
        return summ;

    }
}


